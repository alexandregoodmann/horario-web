import { Injectable } from '@angular/core';
import { Disciplina } from '../model/disciplina';
import { Nivel } from '../model/nivel';

@Injectable({
  providedIn: 'root'
})
export class NivelService {

  getNiveis() {
    let mapa = this.getCurriculo();
    let niveis: Nivel[] = [];
    mapa.forEach((v, k) => {
      let nivel = new Nivel();
      nivel.disciplinas = v;
      
      let total: number = 0;
      let cursados: number = 0;
      nivel.disciplinas.forEach(d => {
        let s = parseInt(d.sgCodicred.substring(6));
        total = total + s;
        if (d.cursada){
          cursados = cursados + s;
        }
      });
      nivel.cursados = cursados;
      nivel.creditos = total;
      niveis.push(nivel);
    });
    return niveis;
  }

  private getCurriculo() {
    let mapa: Map<number, Array<Disciplina>> = new Map<number, Array<Disciplina>>();
    for (let i = 1; i <= 10; i++) {
      mapa.set(i, CURRICULO[i]);
    }

    mapa.forEach((v, k) => {
      v.forEach(d => {
        this.setClass(d);
      })
    })

    return mapa;
  }

  private setClass(disc: Disciplina): void {
    let clazz = 'list-group-item';
    if (disc.stStatus === 'CURSADA') {
      clazz = clazz + ' cursado';
      disc.cursada = true;
    } else {
      clazz = clazz + ' pendente';
    }
    disc.clazz = clazz;
  }

  getDependencias(sgCodicred: string): Disciplina[] {
    let ret: Disciplina[] = [];
    PERCURSO.planejamentoList.forEach(p => {
      p.disciplinaDtoList.forEach(d => {
        let s = d.sgCodi + '-' + d.sgCred;
        if (sgCodicred === s) {
          let vet_codcred: string[] = [];
          vet_codcred = vet_codcred.concat(this.getCodCred(d.disciplinasCoRequisitos));
          vet_codcred = vet_codcred.concat(this.getCodCred(d.disciplinasPreRequisito));
          vet_codcred = vet_codcred.concat(this.getCodCred(d.disciplinasRequisitosEspeciais));
          if (vet_codcred.length > 0) {
            vet_codcred.forEach(codcred => {
              if (codcred !== '') {
                ret.push(this.getDisciplinaCurriculo(codcred));
              }
            });
          }
        }
      });
    });
    return ret;
  }

  private getDisciplinaCurriculo(sgCodCred: string): Disciplina {
    let ret;
    this.getCurriculo().forEach((nivel, k) => {
      nivel.forEach(d => {
        if (d.sgCodicred === sgCodCred) {
          ret = d;
        }
      });
    });
    return ret;
  }

  private getCodCred(s: string): string[] {
    let ret: string[] = [];
    let vet = s.split('\n');
    vet.forEach(v => {
      if (v !== '') {
        ret.push(v.substring(0, 8));
      }
    });
    return ret;
  }

}

